    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
//        System.out.println("full name: " +txtAllname.getText());
//        JOptionPane.showMessageDialog(null, "this is the information IÂ´m going to save " +txtAllname.getText() + 
//                "\nGender " +cmbGender.getSelectedItem());
        String dataToSave = "this is the information we are saving " + "\n"
                + txtAllname.getText() + "\n"
                + cmbGender.getSelectedItem() + "\n"
                + txtID.getText() + "\n"
                + txtEmail.getText() + "\n"
                + txtPhone.getText() + "\n"
                + txtAddress.getText();
        int selection = JOptionPane.showConfirmDialog(null, dataToSave, "Contacts Saving", JOptionPane.YES_NO_CANCEL_OPTION);
        if (selection == 0) {
            JOptionPane.showMessageDialog(null, "information was saved", txtAddress.getText() + "Saved", JOptionPane.INFORMATION_MESSAGE);
            emptyFields();

        } else if (selection == 1) {
            JOptionPane.showMessageDialog(null, "information was NOT saved", txtAddress.getText() + "NOT saved", JOptionPane.ERROR_MESSAGE);

            emptyFields();
        } else {
            JOptionPane.showMessageDialog(null, "Action was canceled", txtAddress.getText() + "Cancelled", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnSaveActionPerformed
